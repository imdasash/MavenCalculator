package com.calculator;

/**
 * Calculator app
 *
 */
public class App {

	public int sum(int a, int b) {
		return a + b;
	}

	public int subtraction(int a, int b) {
		return a - b;
	}

	public int multiplication(int a, int b) {
		return a * b;
	}

	public int divison(int a, int b) {
		return a / b;
	}

}
